     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $thisProduct = Product::find($id); // get product info
        // check
        if ($thisProduct) {
            // it's not null
            $data = [
                'product' => $thisProduct
            ];
            return view('products.show', $data);
        }

        // it's null -> 404
        abort(404);
    }

